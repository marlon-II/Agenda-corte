    private void BTCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTCadastrarActionPerformed
       agendamento ag = new agendamento();
       Procuras pr = new Procuras();
       Cadastramento cd = new Cadastramento();
       cliente c = new cliente();
       cortes co = new cortes();
       funcionarios f = new funcionarios();
       
        try {
            c.setNome(TXTNome.getText());
            pr.ProcurarCliente(c);
            
            co.setNomecorte(TXTCorte.getText());
            pr.ProcurarCorte(co);
            
            f.setNome(TXTBarbeiro.getText());
            pr.ProcurarFuncionario(f);
        } catch (SQLException ex) {
            Logger.getLogger(TelaAgendamento.class.getName()).log(Level.SEVERE, null, ex);
        }
       
       
       ag.setIDCliente(c.getID());
       ag.setData(TXTData.getText());
       ag.setHora(TXTHora.getText());
       ag.setCorte(co.getID());
       ag.setIDFuncionario(f.getID());
       
       
       
      
        try {
            cd.Agendar(ag);
        } catch (SQLException ex) {
            Logger.getLogger(TelaAgendamento.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ParseException ex) {
            Logger.getLogger(TelaAgendamento.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_BTCadastrarActionPerformed

